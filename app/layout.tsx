export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html
      lang="en"
      className={`${geistSans.variable} ${geistMono.variable}`}
      suppressHydrationWarning
    >
      <head>
        {/* Anti-flash theme bootstrap.
            Runs synchronously BEFORE React hydrates and BEFORE first paint —
            so the user never sees a dark frame flicker through to light (or
            vice versa) on page load. Reads the saved choice from localStorage,
            falls back to the OS preference if nothing's saved. */}
        <script
          dangerouslySetInnerHTML={{
            __html: `(function(){try{var t=localStorage.getItem('dezignxo.theme');if(!t){t=window.matchMedia('(prefers-color-scheme: light)').matches?'light':'dark';}document.documentElement.setAttribute('data-theme',t);}catch(e){document.documentElement.setAttribute('data-theme','dark');}})();`,
          }}
        />
        {/* Site-wide structured data — Organization + WebSite (with a
            SearchAction so Google can render the SERP sitelinks-search
            box for brand queries) + SiteNavigationElement (declares the
            primary nav so Google has explicit signals about which
            internal pages qualify for sitelinks under the brand SERP). */}
        <JsonLd
          schema={[
            buildOrganizationSchema(),
            buildWebSiteSchema(),
            buildSiteNavigationSchema(),
          ]}
        />
      </head>
      <body className="min-h-dvh bg-canvas text-primary antialiased">
        <Providers>{children}</Providers>
      </body>
    </html>
  );
}
